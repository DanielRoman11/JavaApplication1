    private void llenarTabla(ArrayList<Valvula> eLista){
        DefaultTableModel modelo = (DefaultTableModel) this.jVRevisar.getModel();
        
        for (Valvula fila : eLista) {
            modelo.addRow(new Object[]{fila.getCodigo(),fila.getNombre(),fila.getPrecio(),fila.getCantidad()});
            
        }
    }
