    private void jAñadirVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jAñadirVMouseClicked
        if (jVNombre.getText().isBlank() || jVPrecio.getText().isBlank() || jVCantidad.getText().isBlank() || jCodigo.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "");
        }else{
         String nombre = this.jVNombre.getText();
        int precio = Integer.parseInt(this.jVPrecio.getText());
        int cantidad = Integer.parseInt(this.jVCantidad.getText());
        int id = Integer.parseInt(this.jCodigo.getText());
        
        ListaValvulas listaValvulas = new ListaValvulas();
        
        listaValvulas.agregarValvula(id, nombre, precio, cantidad);
        
        this.jCodigo.setText(null);
        this.jVNombre.setText(null);
        this.jVPrecio.setText(null);
        this.jVPrecio.setText(null);
        
        llenarTabla(listaValvulas.getLista());
        limpiar();   
        } 
    }//GEN-LAST:event_jAñadirVMouseClicked
    
