    private void jVPrecioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jVPrecioKeyTyped
        char car;
        car = evt.getKeyChar();
        if((car<'0' || car>'9') && car!=(char)KeyEvent.VK_BACK_SPACE){
            evt.consume();
            JOptionPane.showMessageDialog(this, "Solo se admiten números","Validar información",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jVPrecioKeyTyped

