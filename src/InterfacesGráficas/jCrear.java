    private void jAñadirBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jAñadirBtnMouseClicked
        if (jCntNombre.getText().isBlank()|| jCntApellido.getText().isBlank() || jClntEdad.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos","Validar información",JOptionPane.INFORMATION_MESSAGE);
        } else{
            Cliente cliente = new Cliente(listaClientes.size()+1,jCntNombre.getText(),jCntApellido.getText(),Integer.parseInt(jClntEdad.getText()));
            listaClientes.add(cliente);
            
            mostrarClientes();
            limpiar();
        }
        
    }//GEN-LAST:event_jAñadirBtnMouseClicked
    public void mostrarClientes(){
        String matris[][] = new String[listaClientes.size()][4];
        for (int i = 0; i < listaClientes.size(); i++) {
            matris[i][0]=String.valueOf((listaClientes.get(i).getId()));
            matris[i][1]=listaClientes.get(i).getNombre();
            matris[i][2]=listaClientes.get(i).getApellido();
            matris[i][3]=String.valueOf(listaClientes.get(i).getEdad());
        }
        jTablaClientes.setModel(new javax.swing.table.DefaultTableModel(
            matris,
            new String [] {
                "ID", "Nombre", "Apellido", "Edad"
            }
        ));
    }
